<br>
 	Electrical and Electronics Engineering forms the application of the laws of Physics governing electricity, magnetism, and light to develop new technologies in helping mankind. The Electrical Engineering discipline has its esoteric side, with electrical engineers being involved in the generation, protection, transmission, distribution and application of electrical energy. However, on the mundane side, they are also involved in that aspect that lights our homes, cooks our food and drives our machinery. They also make very important contributions in telecommunication, television, computer technology, medicine, education, space exploration, defense and other critical sectors of our society.

             Many scientific fields depend on electrical engineers for their design, analysis and maintenance. Engineers who specialize in Electrical and Electronics Engineering are concerned with the design and analysis of controllers, power converters that eventually help in obtaining the desired characteristics of electromechanical systems. Today, Electrical engineering has a wide reach in all spheres of technology. As an Electrical and Electronic Engineering student at Government Polytechnic College Muttom, you will work closely with top-ranked faculty and gain the fundamental knowledge and theories that govern modern Electrical engineering.

