The curriculum of the three year diploma course in computer technology is updated from time to time to keep up with the ever evolving IT sector. Emphasis is laid not only on the theoretical concepts but also on practical experiences and industry interactions also. The students excel in the area of software development and are well equipped to build the best quality software products. The syllabus covers areas like Basic engineering, Digital computer principles, Data communication, Programming languages including java and C, Computer architecture, Database management system, Operating systems, Embedded systems, Software engineering, Information security, Web technology, Network programming etc. In addition to these, students also gain valuable experiences in leadership and teamwork through the team projects, presentations and extracurricular activities.

             On completion of the course one can get job as software developers, programmers, web developers, web masters, software engineers, system analysts, database administrators, and system programmers etc. Also they have the opportunity for opting higher studies leading to B.Tech./B.E. in computer science and engineering at various institutions in India including IITs and NITs.<br><br>

